import pandas as pd
import numpy as np

from tsfresh import extract_features
from tsfresh.utilities.dataframe_functions import impute

from sklearn.model_selection import train_test_split
from sklearn.feature_selection import SelectKBest, f_regression

from sklearn.metrics import mean_absolute_error, mean_squared_error,mean_absolute_percentage_error

df = pd.read_excel("Wheat_Punjab_Rabi.xlsx")
df = df.sort_values('year').reset_index(drop=True)

y = df["yield"]
y.index = df["year"]

features = [
    'total_rainfall_curr_11',
    'total_rainfall_curr_12',
    'max_temperature_curr_10',
    'min_temperature_curr_11'
]

df_long = df[['year'] + features].melt(
    id_vars='year',
    var_name='feature_name',
    value_name='value'
)

df_long['id'] = df_long['year']

df_long['time'] = df_long['feature_name'].str.extract(r'(\d+)$').astype(int)

df_long['kind'] = df_long['feature_name'].str.replace(r'_curr_\d+', '', regex=True)

df_long = df_long[['id', 'time', 'kind', 'value']]
df_long = df_long.sort_values(['id', 'kind', 'time'])

features_tsfresh = extract_features(
    df_long,
    column_id='id',
    column_sort='time',
    column_kind='kind',
    column_value='value'
)

impute(features_tsfresh)

X = features_tsfresh
X = X.loc[y.index]

X_train = X[X.index < 2019]
X_test  = X[X.index >= 2019]

y_train = y.loc[X_train.index]
y_test  = y.loc[X_test.index]

def clean_df(df):
    df = df.replace([np.inf, -np.inf], np.nan)
    return df.fillna(df.median(numeric_only=True))

X_train = clean_df(X_train)
X_test = clean_df(X_test[X_train.columns])

from sklearn.preprocessing import RobustScaler
scaler = RobustScaler()
X_train_scaled = scaler.fit_transform(X_train)
X_test_scaled = scaler.transform(X_test)

k = 10

selector = SelectKBest(score_func=f_regression, k=k)

X_train_selected = selector.fit_transform(X_train_scaled, y_train)
X_test_selected = selector.transform(X_test_scaled)
X_train_selected.shape

from sklearn.svm import SVR

svm_model = SVR(kernel='rbf', C=100, epsilon=0.01)

svm_model.fit(X_train_selected, y_train)

y_pred_svm = svm_model.predict(X_test_selected)

mae_svm = mean_absolute_error(y_test, y_pred_svm)
rmse_svm = np.sqrt(mean_squared_error(y_test, y_pred_svm))
mape = mean_absolute_percentage_error(y_test,y_pred_svm) * 100

print("SVM Results:")
print("MAE:", mae_svm)
print("RMSE:", rmse_svm)
print("MAPE:",mape)